		<div class="framing-box">
			<h3>Core tension</h3>
			<p>
				In much of the current economy, the costs of acting responsibly are often immediate and concentrated, while the benefits are delayed, collective, and frequently too dispersed to feel economically meaningful for any single actor.
			</p>

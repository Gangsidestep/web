		<h2 class="header_two">Reconnaître le capital naturel comme richesse de base</h2>
		<span class="span_text_box">
			Si l’on veut redéfinir la valeur, il faut d’abord clarifier ce qui compte réellement, et pour qui.<br><br>

			Nos économies reposent sur plusieurs formes de capital interdépendantes. Le capital financier est mesuré avec précision.
			Le capital humain et social est de plus en plus reconnu, mais encore inégalement intégré dans les décisions.
			Le capital naturel — climat stable, biodiversité, sols, cycles de l’eau — demeure, lui, partiellement invisible dans les prix et la comptabilité institutionnelle.<br><br>

			Or la nature n’est pas un intrant externe : elle constitue la condition de possibilité de toute activité économique.
			Quand cette réalité n’est pas reflétée dans les signaux de valeur, le système produit mécaniquement des décisions qui fragilisent ses propres fondations.<br><br>

			Reconnaître la nature comme patrimoine de base change donc la logique : on passe de la correction a posteriori des dommages à une organisation économique orientée vers la santé du système dans la durée.
		</span>

		<h2 class="header_two">Dividende citoyen pour la Nature : une reconnaissance structurelle</h2>
		<span class="span_text_box">
			C’est dans ce cadre qu’émerge la notion de <strong>dividende citoyen pour la Nature</strong>.<br><br>

			L’objectif n’est pas de créer artificiellement de la valeur, mais de rendre visible et économiquement lisible la valeur économique réelle générée par des systèmes naturels en bonne santé.<br><br>

			Ce dividende n’est ni conçu comme une subvention ni comme une compensation ponctuelle. Il constitue une reconnaissance structurelle du fait que les systèmes naturels représentent une richesse commune — et que toute activité économique qui en découle implique une responsabilité réciproque. En ce sens, le dividende exprime à la fois un droit et une obligation : le droit de bénéficier de systèmes régénératifs, et l’obligation de contribuer à leur continuité.<br><br>

			Ce n’est pas d’abord un appel moral : c’est une question d’architecture économique. Comment rendre la valeur écologique suffisamment lisible pour qu’elle puisse dialoguer — et compléter — les signaux déjà puissants par lesquels la bonne gestion est reconnue dans le secteur privé (salaires, profits, dividendes, confiance des investisseurs) ?<br><br>

			Du point de vue de la gouvernance, la légitimité à long terme d’un tel mécanisme ne peut pas reposer uniquement sur l’adoption privée. Elle dépend également d’une légitimité publique issue des citoyens et du processus démocratique, tandis que les institutions scientifiques et publiques contribuent à définir comment la valeur écologique est mesurée, validée et progressivement traduite en cadres robustes.<br><br>

			La mise en œuvre durable d’un tel mécanisme supposerait ainsi une forme de stewardship institutionnel. Sa légitimité démocratique reposerait sur les citoyens et la gouvernance publique, tandis que les institutions scientifiques contribueraient à définir et affiner les indicateurs écologiques sous-jacents au dividende. Sur le plan opérationnel, le mécanisme pourrait s’appuyer sur des infrastructures financières et numériques capables de traduire les signaux de valeur écologique dans les transactions économiques quotidiennes — afin que la reconnaissance du capital naturel s’inscrive concrètement dans l’économie réelle plutôt que de rester un exercice comptable abstrait.
		</span>

		<h2 class="header_two">Passer de la théorie à la mise en œuvre</h2>
		<span class="span_text_box">
			Une théorie du changement n’a de valeur que si elle s’incarne dans des mécanismes opérant dans l’économie ordinaire.<br><br>

			Si le capital naturel est une richesse fondatrice, sa valeur doit apparaître non seulement dans les textes réglementaires,
			mais aussi dans les décisions courantes des ménages, des entreprises et des communautés.
			Ce sont les prix, incitations et circuits de bénéfices qui orientent les comportements réels.<br><br>

			L’alignement systémique suppose donc une transformation progressive de ces signaux :
			moins de prime implicite à l’extraction, plus de cohérence économique en faveur de la régénération.<br><br>

			Des initiatives comme <a href="https://mydio.com/" class="link" target="_blank" rel="noopener noreferrer">mydio.com</a>
			testent précisément cette hypothèse dans des transactions concrètes, en reliant bénéfice immédiat et trajectoire écologique de long terme.<br><br>

			Une image utile est celle d’une TVA dynamique orientée Nature :
			dans un schéma classique, la valeur ajoutée est financée par davantage d’effort monétaire du consommateur ;
			dans un schéma de dividende citoyen pour la Nature, une part croissante de cette valeur peut être portée par les gains collectifs issus d’une meilleure préservation du capital naturel.
			Plus la comptabilité écologique devient robuste, moins l’ajustement doit rester volatil.<br><br>

			La mise en œuvre ne remplace pas la théorie : elle la met à l’épreuve, permet l’apprentissage continu et renforce la redevabilité.<br><br>

            <b>En résumé, la logique du changement est la suivante :</b> lorsque les mécanismes économiques rendent visible et partageable la valeur du capital naturel, les signaux de valeur qui orientent les décisions quotidiennes évoluent. Les comportements individuels et organisationnels commencent alors à intégrer plus systématiquement les bénéfices de la régénération. À mesure que ces signaux deviennent plus cohérents et plus répandus dans l’économie réelle, l’équilibre économique lui-même se déplace progressivement d’une logique d’extraction rentable vers une logique de régénération viable.
		</span>

		<h2 class="header_two">Mesurer les progrès systémiques</h2>
		<span class="span_text_box">
			Une théorie du changement crédible doit pouvoir être testée empiriquement.<br><br>

			Un indicateur central est le volume et la valeur des transactions passant par des mécanismes qui reconnaissent explicitement le capital naturel.
			Cet indicateur mesure à la fois l’adoption et la lisibilité économique croissante de la valeur écologique.<br><br>

			D’autres indicateurs complètent l’analyse : progression de la participation active (citoyens et entreprises),
			part des dépenses influencées par des incitations régénératives, et évolution observable des choix de consommation.
			L’idée est d’observer une bascule graduelle de la logique « extraction rentable » vers la logique « régénération viable ». <br><br>

			Les résultats environnementaux comptent aussi : émissions carbone, indicateurs biodiversité, et méthodes de comptabilité du capital naturel.
			Aucun indicateur n’est parfait dès le départ ; l’important est de construire une infrastructure d’incitation capable d’intégrer des mesures de plus en plus robustes avec le temps.
		</span>

		<h2 class="header_two">Direction stratégique : une économie réellement régénérative</h2>
		<span class="span_text_box">
			La transformation systémique ne se décrète pas. Elle se construit par réalignements successifs des incitations, des institutions et des pratiques quotidiennes.<br><br>

			Cette théorie du changement n’est donc pas un plan figé, mais un cadre évolutif.
			Elle repose sur une conviction simple : la prospérité durable dépend de notre capacité à traiter les systèmes naturels, sociaux et humains comme de vraies formes de richesse,
			et à organiser leur régénération dans l’économie elle-même.<br><br>

			À mesure que les initiatives de terrain testent ces principes, le cadre s’affine.
			Son rôle est moins d’imposer une solution unique que d’indiquer une direction :
			vers une économie où la régénération est soutenue structurellement, où la responsabilité est partagée,
			et où la valeur est définie de manière assez large pour préserver l’avenir qu’elle prétend servir.
		</span>


<h1 class="header_one">Une économie qui fonctionne pour la Nature</h1>
<p></p>

<span class="span_text_box">
	Notre crise écologique n'est pas seulement environnementale — elle est économique. Ce site porte sur l'<strong>alignement de l'économie et de la Nature</strong> :
	comment un <strong>dividende citoyen pour la Nature</strong> peut recadrer les incitations, renforcer la comptabilité du capital naturel et intégrer
	la valeur écologique dans les décisions du quotidien.
</span>

<p></p>

<span class="span_text_box">
	Les marchés excellent à tarifer l'extraction, mais peinent souvent à reconnaître la préservation. Si la Nature sous-tend toute richesse,
	pourquoi est-elle largement absente de la manière dont la richesse est mesurée, distribuée et encouragée ? Si nous voulons des systèmes économiques
	résilients et équitables, nous avons besoin de meilleurs signaux de valeur — là où les décisions se prennent : en politique, en finance et dans les échanges quotidiens.
</span>

<p></p>

<span class="span_text_box">
	<i>My Drop In the Oceans</i> explore une réponse systémique : un cadre conçu pour aligner les incitations économiques avec la réalité écologique,
	en intégrant la Nature dans la comptabilité, les institutions et des formes de reconnaissance financière qui contribuent à normaliser la préservation
	comme choix économique rationnel.
</span>

<p></p>

<span class="span_text_box">
	Pour approfondir, lisez le <a href="/fr/theory-of-change/" style="text-decoration:underline;">cadre de la théorie du changement pour aligner les incitations</a>.
	Si vous souhaitez explorer des recherches, des projets pilotes ou des partenariats, <a href="/fr/contact/" style="text-decoration:underline;">contactez My Drop In the Oceans</a>.
</span>


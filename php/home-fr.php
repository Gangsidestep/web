<h2 class="header_two">Questions fréquentes</h2>
<p></p>

<span class="span_text_box">
	<strong>Est-ce un revenu universel de base ?</strong><br>
	Non. Bien qu'il utilise le langage d'un dividende, l'accent est mis sur la comptabilité écologique et la conception des incitations :
	aligner les signaux économiques sur la valeur du capital naturel et de la préservation.
</span>

<p></p>

<span class="span_text_box">
	<strong>En quoi est-ce différent de la tarification du carbone ?</strong><br>
	La tarification du carbone traite une externalité spécifique. Un dividende citoyen pour la Nature aborde la question plus large de la façon dont la richesse écologique
	est reconnue, mesurée et distribuée dans les structures économiques.
</span>

<p></p>

<span class="span_text_box">
	<strong>Pourquoi lier cela aux transactions quotidiennes ?</strong><br>
	Parce que les incitations fonctionnent là où les personnes et les organisations prennent des décisions. Intégrer la valeur écologique dans les échanges quotidiens aide à combler le fossé
	entre les bénéfices à long terme et la reconnaissance financière immédiate.
</span>


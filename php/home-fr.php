<h2 class="header_two">Dividendes pour la Nature</h2>
<p></p>

<span class="span_text_box">
	Un dividende citoyen pour la Nature n'est pas un geste charitable. C'est un ajustement structurel de la façon dont la valeur est comptabilisée.
	Si les écosystèmes génèrent une richesse publique mesurable — stabilité, résilience, productivité — alors cette valeur devrait se refléter
	dans les flux économiques et dans la manière dont les bénéfices sont partagés.
</span>

<p></p>

<span class="span_text_box">
	Cette réflexion s'appuie sur une longue tradition intellectuelle et pratique. Les premières initiatives telles que
	<a href="https://www.teebweb.org/" target="_blank" rel="noopener noreferrer" style="text-decoration:underline;">TEEB (L'économie des écosystèmes et de la biodiversité)</a>
	ont contribué à formuler les écosystèmes en termes économiques. Dans le secteur privé, des initiatives comme la comptabilité
	Profit &amp; Perte Environnemental (EP&amp;L) de Puma ont démontré que les externalités environnementales pouvaient être mesurées et tarifées.
	Les contributions de la société civile — notamment les travaux du
	<a href="https://www.footprintnetwork.org/" target="_blank" rel="noopener noreferrer" style="text-decoration:underline;">Global Footprint Network</a>
	et du Jour du Dépassement de la Terre — ont traduit les limites planétaires en métriques tangibles.
</span>

<p></p>

<span class="span_text_box">
	Plus récemment, des cadres tels que la
	<a href="https://www.gov.uk/government/publications/final-report-the-economics-of-biodiversity-the-dasgupta-review" target="_blank" rel="noopener noreferrer" style="text-decoration:underline;">Revue Dasgupta</a>,
	le <a href="https://seea.un.org/" target="_blank" rel="noopener noreferrer" style="text-decoration:underline;">cadre SEEA des Nations Unies</a>
	et l'<a href="https://www.ipbes.net/node/35274" target="_blank" rel="noopener noreferrer" style="text-decoration:underline;">Évaluation mondiale de l'IPBES</a>
	ont renforcé et institutionnalisé cette direction.
	Ensemble, ils convergent vers la même conclusion : le capital naturel est fondamental pour la prospérité, mais reste structurellement sous-évalué dans la prise de décision économique.
</span>

<p></p>

<span class="span_text_box">
	Le concept de dividende consiste moins à « partager » ce que la Nature nous donne déjà qu'à veiller à ce que la valeur de la Nature soit reconnue
	dans des termes économiques capables de rivaliser avec les mesures conventionnelles de performance. Autrement dit : une conception institutionnelle qui rend
	la préservation financièrement lisible — de la même manière qu'une bonne gestion d'entreprise peut se traduire par des salaires, des bénéfices et des dividendes plus élevés.
</span>

<p></p>

<span class="span_text_box">
	La clé est la légitimité à grande échelle : des normes partagées de mesure et d'évaluation, une gouvernance qui mérite la confiance du public, et
	des mécanismes qui traduisent les résultats écologiques en valeur économique tangible. Sans cela, le « capital naturel » reste abstrait
	et ne peut rivaliser avec les signaux financiers qui orientent actuellement les décisions.
</span>

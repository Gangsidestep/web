<h2 class="header_two">Dividenden für die Natur</h2>
<p></p>

<span class="span_text_box">
	Eine Bürgerdividende für die Natur ist keine caritative Geste. Es ist eine strukturelle Anpassung der Art, wie Wert bilanziert wird.
	Wenn Ökosysteme messbaren öffentlichen Reichtum erzeugen — Stabilität, Resilienz, Produktivität — dann sollte dieser Wert sich in wirtschaftlichen Strömen
	und in der Art, wie Gewinne geteilt werden, widerspiegeln.
</span>

<p></p>

<span class="span_text_box">
	Dieses Denken baut auf einer längeren intellektuellen und praktischen Tradition auf. Frühe Initiativen wie
	<a href="https://www.teebweb.org/" target="_blank" rel="noopener noreferrer" style="text-decoration:underline;">TEEB (Die Ökonomie der Ökosysteme und der Biodiversität)</a>
	halfen dabei, Ökosysteme in wirtschaftlichen Begriffen zu formulieren. Im Privatsektor demonstrierten Initiativen wie
	die Environmental Profit &amp; Loss (EP&amp;L) Bilanzierung von Puma, dass Umweltexternalitäten gemessen und bepreist werden können.
	Beiträge der Zivilgesellschaft — einschliesslich der Arbeit des
	<a href="https://www.footprintnetwork.org/" target="_blank" rel="noopener noreferrer" style="text-decoration:underline;">Global Footprint Network</a>
	und des Earth Overshoot Day — übersetzten planetare Grenzen in greifbare Metriken.
</span>

<p></p>

<span class="span_text_box">
	Jüngere Rahmen wie die
	<a href="https://www.gov.uk/government/publications/final-report-the-economics-of-biodiversity-the-dasgupta-review" target="_blank" rel="noopener noreferrer" style="text-decoration:underline;">Dasgupta-Überprüfung</a>,
	der <a href="https://seea.un.org/" target="_blank" rel="noopener noreferrer" style="text-decoration:underline;">UN-SEEA-Rahmen</a>
	und die <a href="https://www.ipbes.net/node/35274" target="_blank" rel="noopener noreferrer" style="text-decoration:underline;">IPBES-Globalbewertung</a>
	haben diese Richtung verstärkt und institutionalisiert.
	Gemeinsam kommen sie zur selben Schlussfolgerung: Naturkapital ist grundlegend für Wohlstand, bleibt aber strukturell unterbewertet in der wirtschaftlichen Entscheidungsfindung.
</span>

<p></p>

<span class="span_text_box">
	Das Dividendenkonzept geht weniger darum, das zu „teilen", was die Natur uns bereits gibt, sondern vielmehr darum sicherzustellen, dass der Wert der Natur
	in wirtschaftlichen Begriffen anerkannt wird, die mit konventionellen Leistungsmassen mithalten können. Mit anderen Worten: institutionelles Design, das
	Stewardship finanziell lesbar macht — vergleichbar damit, wie gute Unternehmenssteuerung in höhere Löhne, Gewinnne und Dividenden umgemünzt werden kann.
</span>

<p></p>

<span class="span_text_box">
	Der Schlüssel liegt in Legitimität in grossem Massstab: gemeinsame Standards für Messung und Bewertung, Governance, die öffentliches Vertrauen verdient, und
	Mechanismen, die ökologische Ergebnisse in greifbaren wirtschaftlichen Wert übersetzen. Ohne das bleibt „Naturkapital" abstrakt
	und kann nicht mit den Finanzsignalen mithalten, die derzeit Entscheidungen antreiben.
</span>

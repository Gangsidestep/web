<h2 class="header_two">Häufig gestellte Fragen</h2>
<p></p>

<span class="span_text_box">
	<strong>Ist das ein universelles Grundeinkommen?</strong><br>
	Nein. Obwohl es die Sprache einer Dividende verwendet, liegt der Fokus auf ökologischer Bilanzierung und Anreizgestaltung:
	wirtschaftliche Signale mit dem Wert von Naturkapital und Stewardship in Einklang bringen.
</span>

<p></p>

<span class="span_text_box">
	<strong>Wie unterscheidet sich das von der CO₂-Bepreisung?</strong><br>
	Die CO₂-Bepreisung behandelt eine spezifische Externalität. Eine Bürgerdividende für die Natur behandelt die umfassendere Frage, wie ökologischer Reichtum
	in wirtschaftlichen Strukturen anerkannt, gemessen und verteilt wird.
</span>

<p></p>

<span class="span_text_box">
	<strong>Warum dies mit alltäglichen Transaktionen verknüpfen?</strong><br>
	Weil Anreize dort wirken, wo Menschen und Organisationen Entscheidungen treffen. Ökologischen Wert in den täglichen Austausch zu bringen, hilft dabei, die Lücke
	zwischen langfristigen Vorteilen und unmittelbarer finanzieller Anerkennung zu schliessen.
</span>


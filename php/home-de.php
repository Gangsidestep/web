<h1 class="header_one">Eine Wirtschaft, die für die Natur funktioniert</h1>
<p></p>

<span class="span_text_box">
	Unsere ökologische Krise ist nicht nur eine Umweltkrise — sie ist eine Wirtschaftskrise. Diese Website befasst sich mit der <strong>Ausrichtung von Wirtschaft und Natur</strong>:
	wie eine <strong>Bürgerdividende für die Natur</strong> Anreize neu gestalten, die Bilanzierung von Naturkapital stärken und
	ökologische Werte in alltägliche Entscheidungen einbetten kann.
</span>

<p></p>

<span class="span_text_box">
	Märkte sind hervorragend darin, Extraktion zu bepreisen, versagen aber oft darin, Stewardship anzuerkennen. Wenn Natur den gesamten Reichtum begründet,
	warum ist sie dann weitgehend absent aus der Art, wie Reichtum gemessen, verteilt und angereizt wird? Wenn wir widerstandsfähige und gerechte Wirtschaftssysteme wollen,
	brauchen wir bessere Wertsignale — und zwar dort, wo Entscheidungen getroffen werden: in der Politik, im Finanzwesen und im täglichen Austausch.
</span>

<p></p>

<span class="span_text_box">
	<i>My Drop In the Oceans</i> erforscht eine systemische Antwort: ein Rahmen, der wirtschaftliche Anreize mit der ökologischen Realität in Einklang bringen soll,
	indem Natur in die Rechnungslegung, Institutionen und Formen der finanziellen Anerkennung eingebettet wird, die dazu beitragen, Stewardship als rationale wirtschaftliche Entscheidung zu normalisieren.
</span>

<p></p>

<span class="span_text_box">
	Um tiefer einzusteigen, lesen Sie den <a href="/de/theory-of-change/" style="text-decoration:underline;">Theorie-des-Wandels-Rahmen zur Ausrichtung von Anreizen</a>.

<p>Une phrase de Jean-Pierre Danthine, dans son récent entretien avec SwissPowerShift sur l'intégration de la durabilité dans le mandat de la Banque nationale suisse, mérite qu'on s'y attarde :</p>

<blockquote>« Si cela coûte cher et que le bénéfice est lointain et diffus, l'engagement disparaît. »</blockquote>

<p>Cette observation capture silencieusement l'une des tensions les plus profondes de la transition vers la durabilité.</p>

<p>Car, en pratique, une grande partie de notre économie moderne fonctionne encore selon <a class="blog-nav-link" href="<?php echo htmlspecialchars(localized_path('/blog/make-money-first-then-think-of-nature/', $locale)); ?>">une séquence familière</a> :</p>

<blockquote>D'abord gagner de l'argent.<br>
Puis — s'il reste assez — penser à la Nature.</blockquote>

<p>Elle est rarement formulée aussi directement.<br>
Mais, structurellement, c'est souvent ainsi que le système fonctionne.</p>

<p>Les entreprises sont créées pour générer du profit.<br>
Les individus travaillent pour sécuriser un revenu.<br>
Les gouvernements mesurent la réussite par la performance économique.</p>

<p>Ce n'est qu'une fois ces objectifs sécurisés — ou jugés stables — que l'on demande :</p>

<ul>
  <li>Pouvons-nous nous permettre de réduire les émissions ?</li>
  <li>Pouvons-nous nous permettre de protéger la biodiversité ?</li>
  <li>Pouvons-nous nous permettre une agriculture régénérative ?</li>
  <li>Pouvons-nous nous permettre d'intégrer la durabilité dans les décisions financières ?</li>
</ul>

<p>La Nature entre alors dans la conversation comme un coût.<br>
Une contrainte.<br>
Une réflexion secondaire.</p>

<p>Et ce n'est pas parce que les gens ne s'en soucient pas.</p>
<p>C'est parce que le système rend risqué le fait de s'en soucier en premier.</p>

<h2 class="header_two">Le niveau manquant dans le débat suisse sur la durabilité</h2>

<p>L'entretien SwissPowerShift soulève à juste titre des questions de responsabilité macroéconomique :</p>

<ul>
  <li>la tarification du carbone,</li>
  <li>l'investissement public,</li>
  <li>le rôle de l'État,</li>
  <li>les limites de la finance volontaire,</li>
  <li>et la question de savoir si la Banque nationale suisse devrait intégrer la durabilité dans son mandat.</li>
</ul>

<p>Ces questions comptent.</p>

<p>Mais elles opèrent en grande partie au niveau institutionnel de l'économie.</p>

<p>Or, la plupart des gens vivent l'économie ailleurs :</p>

<ul>
  <li>au supermarché,</li>
  <li>en payant leur loyer,</li>
  <li>en choisissant leur transport,</li>
  <li>en réservant un voyage,</li>
  <li>en achetant des produits,</li>
  <li>ou simplement en essayant de boucler le mois.</li>
</ul>

<p>C'est précisément là que l'observation de Danthine prend toute son importance.</p>

<p>Si la durabilité apparaît essentiellement comme :</p>

<ul>
  <li>un sacrifice,</li>
  <li>un coût supplémentaire,</li>
  <li>une prime de prix,</li>
  <li>une restriction,</li>
  <li>ou une obligation morale,</li>
</ul>

<p>tandis que les bénéfices restent lointains, abstraits et collectifs, alors un alignement sociétal large devient extrêmement difficile.</p>

<p>Non parce que les gens ne s'en soucient pas.</p>

<p>Mais parce que les signaux économiques qu'ils rencontrent chaque jour continuent à renforcer une autre logique.</p>

<h2 class="header_two">Le dilemme individuel</h2>

<p>Un individu peut vouloir :</p>

<ul>
  <li>acheter bio,</li>
  <li>voyager moins,</li>
  <li>investir de manière responsable,</li>
  <li>soutenir des marques durables,</li>
  <li>ou choisir une mobilité à moindre impact.</li>
</ul>

<p>Mais les contraintes de revenu sont réelles — et immédiates.<br>
Le loyer est réel.<br>
Les frais de santé sont réels.<br>
Les factures d'énergie sont réelles.</p>

<p>Les coûts pour la Nature, à l'inverse, sont souvent de long terme et seulement partiellement reflétés dans les signaux de marché actuels — reportés plutôt que ressentis au moment de la transaction.</p>

<p>Quand les budgets sont serrés, la durabilité devient conditionnelle :</p>

<blockquote>« Je ferai le choix durable — si je peux me le permettre. »</blockquote>

<p>Et, de nouveau, la Nature devient ce que l'on considère <em>après</em> la sécurité financière.</p>

<p>Le système nous enseigne discrètement que <a class="blog-nav-link" href="<?php echo htmlspecialchars(localized_path('/blog/nature-is-not-a-charity/', $locale)); ?>">prendre soin de la Nature est un bien de luxe</a>.</p>

<p>Pourtant, les conséquences de la dégradation environnementale ne sont pas un luxe.<br>
Elles touchent les systèmes alimentaires, la santé, l'eau, la stabilité climatique et la prospérité de long terme.</p>

<p>La contradiction est évidente.</p>
<p>Les incitations, elles, ne le sont pas.</p>

<h2 class="header_two">Un problème de conception économique</h2>

<p>Une grande partie du débat environnemental se concentre sur les comportements :</p>

<p>Pourquoi les consommateurs ne changent-ils pas plus vite ?<br>
Pourquoi les entreprises n'agissent-elles pas plus fortement ?<br>
Pourquoi les gouvernements ne régulent-ils pas davantage ?</p>

<p>Mais sous ces questions se cache un enjeu plus profond de conception économique.</p>

<p>Notre économie récompense d'abord l'extraction financière.<br>
Elle demande ensuite la responsabilité écologique.</p>

<p>Le profit se mesure au trimestre.<br>
La Nature se régénère sur des décennies.</p>

<p>Les rendements financiers sont obligatoires.<br>
<a class="blog-nav-link" href="<?php echo htmlspecialchars(localized_path('/blog/what-if-stewardship-was-financially-recognised/', $locale)); ?>">La préservation environnementale</a> reste souvent optionnelle.</p>

<p>Dans ces conditions de conception, même des acteurs de bonne volonté sont contraints.</p>

<p>Nous demandons aux entreprises et aux individus d'intégrer le coût de la Nature dans un système qui n'a jamais été conçu pour reconnaître cette valeur.</p>

<p>Ce n'est pas seulement difficile.</p>
<p>C'est structurellement incohérent.</p>

<h2 class="header_two">Quand la responsabilité devient un risque</h2>

<p>Si une entreprise augmente volontairement ses coûts pour protéger les écosystèmes, elle risque de perdre des parts de marché.</p>

<p>Si un pays relève ses standards unilatéralement, il risque le désavantage concurrentiel.</p>

<p>Si une personne paie plus cher des biens durables, elle absorbe individuellement la prime.</p>

<p>La responsabilité devient asymétrique.</p>

<p>Et la responsabilité asymétrique ne passe pas à l'échelle.</p>

<p>Cela explique pourquoi la sensibilisation ne se traduit pas automatiquement en changement systémique.</p>

<p>Le problème n'est pas l'incompréhension de la crise.</p>
<p>C'est le désalignement entre responsabilité et récompense.</p>

<p>Nous avons créé une économie où protéger la Nature génère souvent une friction financière — plutôt qu'un renforcement financier.</p>

<h2 class="header_two">La subvention invisible</h2>

<p>Une vérité inconfortable est au centre de cette conversation :</p>

<p>La Nature est encore subventionnée par son invisibilité.</p>

<p>Nous ne payons pas le coût complet de la dégradation des sols.<br>
Nous ne payons pas le coût complet de l'effondrement de la biodiversité.<br>
Nous ne payons pas le coût complet de l'accumulation du carbone.<br>
Nous ne payons pas le coût complet de l'affaiblissement des systèmes naturels dont dépendent nos économies.</p>

<p>Ces coûts sont reportés.</p>

<p>Sur les générations futures.<br>
Sur les communautés vulnérables.<br>
Sur des écosystèmes qui n'ont pas de bilan comptable.</p>

<p>En termes comptables, ils restent hors bilan.</p>

<p>En termes écologiques, ils s'accumulent.</p>

<p>Tant que ces coûts restent externes, l'instruction reste la même :</p>

<blockquote>Gagner de l'argent d'abord.<br>
Penser à la Nature ensuite.</blockquote>

<h2 class="header_two">Du mandat aux choix quotidiens : où la durabilité devient visible</h2>

<p>Le débat autour du rôle de la Banque nationale suisse est important parce qu'il pose la question :</p>

<blockquote>Comment les institutions doivent-elles intégrer la durabilité dans leurs décisions ?</blockquote>

<p>Mais une question tout aussi importante reste largement absente :</p>

<blockquote>Comment les citoyens ordinaires vivent-ils économiquement ce système de valeurs au quotidien ?</blockquote>

<p>À ce jour, nous avons largement construit des systèmes où les individus sont censés absorber des coûts plus élevés, changer leurs habitudes, accepter des contraintes et contribuer à la protection de biens naturels communs — sans partager visiblement la valeur qu'ils contribuent à créer.</p>

<p>C'est l'une des raisons pour lesquelles la transition peine souvent à dépasser les cadres institutionnels pour atteindre une dynamique sociétale large.</p>

<p>Le défi n'est pas seulement de réorienter les capitaux à grande échelle.</p>

<p>C'est aussi de rendre la contribution positive économiquement visible et significative là où les comportements humains se produisent réellement.</p>

<h2 class="header_two">Aligner l'économie et la Nature</h2>

<p>C'est la question centrale de la Théorie du changement développée par My Drop in the Oceans :</p>

<p><a class="blog-nav-link" href="<?php echo htmlspecialchars(localized_path('/theory-of-change/', $locale)); ?>">Théorie du changement — Aligner l'économie et la Nature à grande échelle</a></p>

<p>Le postulat de base est simple :</p>

<p>Si l'économie ne reconnaît systématiquement que l'extraction financière, la consommation à court terme et l'efficacité transactionnelle, alors même les politiques de durabilité les mieux intentionnées continueront de se heurter aux incitations plus profondes ancrées dans la vie économique quotidienne.</p>

<p>Mais si la contribution positive à la Nature et à la société devient économiquement visible dans les transactions quotidiennes elles-mêmes, de nouvelles formes d'alignement deviennent possibles.</p>

<p>Non par la culpabilité.</p>

<p>Non par le sacrifice permanent.</p>

<p>Mais par la participation.</p>

<h2 class="header_two">Inverser la séquence</h2>

<p>Et si nous inversions l'ordre ?</p>

<p>Et si la valeur écologique était intégrée directement dans les flux financiers — plutôt que traitée après coup ?</p>

<p>C'est ici que l'idée d'un <strong>dividende citoyen pour la Nature</strong> devient pertinente.</p>

<p>Au lieu de faire porter l'essentiel du coût de la Nature uniquement aux entreprises ou aux consommateurs, la valeur générée par la préservation et la régénération du capital naturel pourrait être redistribuée sous forme de bénéfice économique tangible.</p>

<p>Dans un tel cadre :</p>

<ul>
  <li>Un comportement durable ne serait plus une pénalité financière.</li>
  <li><a class="blog-nav-link" href="<?php echo htmlspecialchars(localized_path('/blog/what-if-stewardship-was-financially-recognised/', $locale)); ?>">La préservation écologique</a> produirait une valeur mesurable.</li>
  <li>Les citoyens partageraient directement les bénéfices des écosystèmes protégés.</li>
  <li>La contribution positive deviendrait économiquement lisible au moment de la transaction.</li>
</ul>

<p>Au lieu de demander :</p>

<blockquote>« Pouvons-nous nous permettre de protéger la Nature ? »</blockquote>

<p>Nous commencerions à demander :</p>

<blockquote>« Comment structurer l'économie pour que protéger la Nature crée de la valeur pour tous ? »</blockquote>

<p>Le déplacement est subtil mais profond.</p>

<p>Du sacrifice vers l'alignement.<br>
De la vertu volontaire vers la conception systémique.<br>
De l'après-coup vers le fondement.</p>

<h2 class="header_two">Correction macroéconomique et visibilité du quotidien</h2>

<p>La transition vers la durabilité exige probablement les deux à la fois :</p>

<ul>
  <li>une correction institutionnelle descendante,</li>
  <li>et une participation économique ascendante.</li>
</ul>

<p>La tarification du carbone, la réglementation, la préservation, l'investissement public et les débats sur les banques centrales opèrent à un niveau du système.</p>

<p>Mais un autre niveau reste largement non résolu :</p>

<blockquote>Comment les citoyens vivent-ils concrètement la valeur de leur contribution à la résilience de long terme des systèmes dont ils dépendent ?</blockquote>

<p>Tant que cette question ne sera pas abordée plus directement, la durabilité risque de continuer à paraître économiquement distante de la réalité quotidienne de la plupart des citoyens.</p>

<p>Et c'est peut-être là le défi le plus profond qui émerge aujourd'hui <a class="blog-nav-link" href="<?php echo htmlspecialchars(localized_path('/blog/why-the-swiss-said-no-to-free-money/', $locale)); ?>">en Suisse</a>, en Europe et au-delà.</p>

<p>Non pas si la durabilité compte.</p>

<p>Mais si nos systèmes économiques sont capables de rendre cette valeur visible là où les décisions humaines sont réellement prises.</p>

<h2 class="header_two">Une question de structure</h2>

<p>Nous demandons souvent :</p>
<p>Pourquoi les gens ne se soucient-ils pas suffisamment ?</p>

<p>Mais la question la plus importante est peut-être :</p>

<p><strong>Pourquoi le système rend-il le soin de la Nature économiquement risqué ?</strong></p>

<p>Tant que protéger la Nature reste structurellement secondaire au profit,<br>
nous continuerons d'osciller entre croissance et culpabilité.</p>

<p>Mais si nous intégrons le capital naturel dans <a class="blog-nav-link" href="<?php echo htmlspecialchars(localized_path('/blog/money-work-worth/', $locale)); ?>">la logique de la récompense économique</a> — si nous reconnaissons que les écosystèmes sont des actifs fondamentaux plutôt que des intrants externes — alors protéger la Nature devient rationnel, non exceptionnel.</p>

<p>Le défi n'est pas de convaincre les gens que la Nature compte.</p>
<p>La plupart le savent déjà.</p>

<p>Le défi est de repenser la séquence.</p>

<p>Pour que l'instruction ne soit plus :</p>

<blockquote>Gagner de l'argent d'abord.<br>
Puis (peut-être) penser à la Nature.</blockquote>

<p>Mais plutôt :</p>

<blockquote>Valoriser et protéger la Nature —<br>
et laisser l'économie refléter cette réalité.</blockquote>

<p>Ce n'est pas un appel moral.</p>
<p>C'est une proposition structurelle.</p>
<p>Et c'est dans la structure que commence le changement durable.</p>

<div class="cta-box">
  <h3>Des cadres institutionnels à la participation</h3>
  <p>Le changement systémique ne commence pas par des déclarations — il commence là où se prennent les décisions économiques du quotidien. Le dividende citoyen pour la Nature est une expression pratique de cette direction, en explorant comment la valeur écologique peut devenir visible dans les transactions ordinaires. Participation, amélioration continue et apprentissage collectif sont essentiels pour façonner des signaux de valeur qui reflètent les réalités dont dépend toute prospérité.</p>
</div>

<div class="framing-box">
  <h3>Concepts clés</h3>
  <p><strong>Capital naturel</strong><br>
  Les systèmes écologiques — stabilité climatique, biodiversité, sols, océans, cycles de l'eau — qui soutiennent toute activité économique.</p>

  <p><strong>Signaux de valeur</strong><br>
  Les repères qui orientent les décisions quotidiennes — prix, incitations, rendements, règles — indiquant ce que le système récompense et ce qu'il relègue au second plan.</p>

  <p><strong>Lisibilité économique</strong><br>
  Le degré auquel une contribution est visible et reconnue dans l'économie — non seulement moralement, mais dans la façon dont les coûts et les bénéfices sont comptabilisés.</p>
</div>


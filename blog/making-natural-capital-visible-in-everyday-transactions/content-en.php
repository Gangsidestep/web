<p>There is a striking phrase in Jean-Pierre Danthine's recent SwissPowerShift interview on whether sustainability should be integrated into the mandate of the Swiss National Bank:</p>

<blockquote>« Si cela coûte cher et que le bénéfice est lointain et diffus, l'engagement disparaît. »</blockquote>

<p>That sentence quietly captures one of the deepest tensions in the sustainability transition.</p>

<p>Because, in practice, much of our modern economy still operates according to <a class="blog-nav-link" href="<?php echo htmlspecialchars(localized_path('/blog/make-money-first-then-think-of-nature/', $locale)); ?>">a familiar sequence</a>:</p>

<blockquote>First make money.<br>
Then — if there is enough left over — think of Nature.</blockquote>

<p>It is rarely stated so bluntly.<br>
Fact is, structurally, that is often how the system works.</p>

<p>Businesses are created to generate profit.<br>
Individuals work to secure income.<br>
Governments measure success through economic performance.</p>

<p>Only once those objectives are secured — or appear stable — do we ask:</p>

<ul>
  <li>Can we afford to reduce emissions?</li>
  <li>Can we afford biodiversity protection?</li>
  <li>Can we afford regenerative agriculture?</li>
  <li>Can we afford to make sustainability part of financial decision-making?</li>
</ul>

<p>Nature enters the conversation as a cost centre.<br>
A constraint.<br>
An afterthought.</p>

<p>And that is not because people do not care.</p>
<p>It is because the system makes it risky to care first.</p>

<h2 class="header_two">The Missing Layer in the Swiss Sustainability Debate</h2>

<p>The SwissPowerShift interview rightly raises questions about macroeconomic responsibility:</p>

<ul>
  <li>carbon pricing,</li>
  <li>public investment,</li>
  <li>the role of the state,</li>
  <li>the limits of voluntary finance,</li>
  <li>and whether the Swiss National Bank should integrate sustainability into its mandate.</li>
</ul>

<p>These questions matter.</p>

<p>But they largely operate at the institutional level of the economy.</p>

<p>Meanwhile, most people experience the economy somewhere else entirely:</p>

<ul>
  <li>at the supermarket,</li>
  <li>when paying rent,</li>
  <li>choosing transport,</li>
  <li>booking travel,</li>
  <li>purchasing products,</li>
  <li>or simply trying to get through the month.</li>
</ul>

<p>This is precisely where Danthine's observation becomes so important.</p>

<p>If sustainability appears primarily as:</p>

<ul>
  <li>a sacrifice,</li>
  <li>an added cost,</li>
  <li>a premium,</li>
  <li>a restriction,</li>
  <li>or a moral obligation,</li>
</ul>

<p>while the benefits remain distant, abstract and collective, then broad societal alignment becomes extremely difficult.</p>

<p>Not because people do not care.</p>

<p>But because the economic signals they encounter every day continue to reinforce another logic entirely.</p>

<h2 class="header_two">The Individual Dilemma</h2>

<p>An individual might want to:</p>

<ul>
  <li>buy organic,</li>
  <li>travel less,</li>
  <li>invest responsibly,</li>
  <li>support sustainable brands,</li>
  <li>or choose lower-impact mobility.</li>
</ul>

<p>But income constraints are real — and immediate.<br>
Rent is real.<br>
Health costs are real.<br>
Energy bills are real.</p>

<p>The costs to Nature, by contrast, are often long-term and only partially reflected in today's market signals — deferred rather than directly felt at the point of transaction.</p>

<p>When budgets are tight, sustainability becomes conditional:</p>

<blockquote>"I'll make the sustainable choice — if I can afford it."</blockquote>

<p>And once again, Nature becomes something we consider <em>after</em> financial security.</p>

<p>The system quietly teaches us that <a class="blog-nav-link" href="<?php echo htmlspecialchars(localized_path('/blog/nature-is-not-a-charity/', $locale)); ?>">caring for Nature is a luxury good</a>.</p>

<p>Yet the consequences of environmental degradation are not luxuries.<br>
They affect food systems, health, water, climate stability, and long-term prosperity.</p>

<p>The contradiction is obvious.</p>
<p>The incentives are not.</p>

<h2 class="header_two">The Design Problem</h2>

<p>Much of the environmental debate focuses on behaviour:</p>

<p>Why don't consumers change faster?<br>
Why don't corporations act more boldly?<br>
Why don't governments regulate more aggressively?</p>

<p>But beneath these questions lies a deeper design issue.</p>

<p>Our economy rewards financial extraction first.<br>
It asks ecological responsibility second.</p>

<p>Profit is measured quarterly.<br>
Nature regenerates over decades.</p>

<p>Financial returns are mandatory.<br>
<a class="blog-nav-link" href="<?php echo htmlspecialchars(localized_path('/blog/what-if-stewardship-was-financially-recognised/', $locale)); ?>">Environmental stewardship</a> is often optional.</p>

<p>Under such design conditions, even well-meaning actors are constrained.</p>

<p>We are asking businesses and individuals to price Nature into a system that was never built to recognise its value in the first place.</p>

<p>That is not just difficult.</p>
<p>It is structurally incoherent.</p>

<h2 class="header_two">When Responsibility Becomes Risk</h2>

<p>If a company voluntarily increases costs to protect ecosystems, it risks losing market share.</p>

<p>If a country raises standards unilaterally, it risks competitive disadvantage.</p>

<p>If an individual pays more for sustainable goods, they personally absorb the premium.</p>

<p>Responsibility becomes asymmetric.</p>

<p>And asymmetric responsibility does not scale.</p>

<p>This helps explain why awareness does not automatically translate into systemic change.</p>

<p>The issue is not that people fail to understand the crisis.</p>
<p>It is that responsibility is misaligned with reward.</p>

<p>We have created an economy where protecting Nature often carries financial friction — rather than financial reinforcement.</p>

<h2 class="header_two">The Invisible Subsidy</h2>

<p>There is an uncomfortable truth at the centre of this conversation:</p>

<p>Nature is still being subsidised by invisibility.</p>

<p>We do not pay the full cost of soil degradation.<br>
We do not pay the full cost of biodiversity collapse.<br>
We do not pay the full cost of carbon accumulation.<br>
We do not pay the full cost of weakening the natural systems on which our economies depend.</p>

<p>Those costs are deferred.</p>

<p>To future generations.<br>
To vulnerable communities.<br>
To ecosystems that do not have balance sheets.</p>

<p>In accounting terms, they remain off-balance-sheet.</p>

<p>In ecological terms, they accumulate.</p>

<p>As long as these costs remain external, the instruction remains the same:</p>

<blockquote>Make money first.<br>
Think of Nature later.</blockquote>

<h2 class="header_two">From Mandates to Daily Decisions: Where Sustainability Becomes Visible</h2>

<p>The debate around the role of the Swiss National Bank is important because it asks:</p>

<blockquote>How should institutions account for sustainability?</blockquote>

<p>But perhaps an equally important question remains largely absent:</p>

<blockquote>How do ordinary people economically experience that value system in daily life?</blockquote>

<p>At present, we have largely built systems where individuals are expected to absorb higher costs, change habits, accept inconvenience and contribute to the protection of shared natural assets — without visibly sharing in the value they help create.</p>

<p>This is one reason why the transition often struggles to move beyond institutional frameworks into broad societal momentum.</p>

<p>The challenge is not only how to redirect capital at scale.</p>

<p>It is also how to make positive contribution economically visible and meaningful at the point where human behaviour actually occurs.</p>

<h2 class="header_two">Aligning Economy and Nature</h2>

<p>This is the central question behind the Theory of Change developed through My Drop in the Oceans:</p>

<p><a class="blog-nav-link" href="<?php echo htmlspecialchars(localized_path('/theory-of-change/', $locale)); ?>">Theory of Change — Aligning Economy and Nature at Scale</a></p>

<p>The underlying premise is simple:</p>

<p>If the economy systematically recognises only financial extraction, short-term consumption and transactional efficiency, then even well-intentioned sustainability policies will continually struggle against the deeper incentives embedded in everyday economic life.</p>

<p>But if positive contribution to Nature and society becomes economically visible within daily transactions themselves, new forms of alignment become possible.</p>

<p>Not through guilt.</p>

<p>Not through permanent sacrifice.</p>

<p>But through participation.</p>

<h2 class="header_two">Reversing the Sequence</h2>

<p>What if we reversed the order?</p>

<p>What if ecological value were embedded directly into financial flows — rather than treated as an afterthought?</p>

<p>This is where the idea of a <strong>Citizen's Dividend for Nature</strong> becomes relevant.</p>

<p>Instead of placing the burden of pricing Nature solely on businesses or individual consumers, the value generated by preserving and regenerating natural capital could be redistributed as tangible economic benefit.</p>

<p>In such a framework:</p>

<ul>
  <li>Sustainable behaviour would not be a financial penalty.</li>
  <li><a class="blog-nav-link" href="<?php echo htmlspecialchars(localized_path('/blog/what-if-stewardship-was-financially-recognised/', $locale)); ?>">Ecological stewardship</a> would generate measurable value.</li>
  <li>Citizens would share directly in the benefits of protected ecosystems.</li>
  <li>Positive contribution would become economically legible at the point of transaction.</li>
</ul>

<p>Rather than asking:</p>

<blockquote>"Can we afford to protect Nature?"</blockquote>

<p>We would begin asking:</p>

<blockquote>"How do we structure the economy so that protecting Nature generates value for everyone?"</blockquote>

<p>The shift is subtle but profound.</p>

<p>From sacrifice to alignment.<br>
From voluntary virtue to systemic design.<br>
From afterthought to foundation.</p>

<h2 class="header_two">Macro Correction and Everyday Visibility</h2>

<p>The sustainability transition likely requires both:</p>

<ul>
  <li>top-down institutional correction,</li>
  <li>and bottom-up economic participation.</li>
</ul>

<p>Carbon pricing, regulation, stewardship, public investment and central bank debates operate at one layer of the system.</p>

<p>But another layer still remains largely unresolved:</p>

<blockquote>How do people tangibly experience the value of contributing to the long-term resilience of the systems they depend on?</blockquote>

<p>Until that question is addressed more directly, sustainability risks continuing to feel economically distant from the daily reality of most citizens.</p>

<p>And perhaps that is the deeper challenge now emerging <a class="blog-nav-link" href="<?php echo htmlspecialchars(localized_path('/blog/why-the-swiss-said-no-to-free-money/', $locale)); ?>">across Switzerland</a>, Europe and beyond.</p>

<p>Not whether sustainability matters.</p>

<p>But whether our economic systems are capable of making that value visible where human decisions are actually made.</p>

<h2 class="header_two">A Question of Structure</h2>

<p>We often ask:</p>
<p>Why don't people care enough?</p>

<p>But perhaps the more important question is:</p>

<p><strong>Why does the system make caring economically risky?</strong></p>

<p>If protecting Nature remains structurally secondary to profit,<br>
we will continue oscillating between growth and guilt.</p>

<p>But if we embed natural capital into <a class="blog-nav-link" href="<?php echo htmlspecialchars(localized_path('/blog/money-work-worth/', $locale)); ?>">the logic of economic reward</a> — if we recognise that ecosystems are foundational assets rather than external inputs — then protecting Nature becomes rational, not exceptional.</p>

<p>The challenge is not persuading people that Nature matters.</p>
<p>Most already know that.</p>

<p>The challenge is redesigning the sequence.</p>

<p>So that the instruction is no longer:</p>

<blockquote>Make money first.<br>
Then (maybe) think of Nature.</blockquote>

<p>But rather:</p>

<blockquote>Value and protect Nature —<br>
and let the economy reflect that reality.</blockquote>

<p>That is not a moral appeal.</p>
<p>It is a structural proposition.</p>
<p>And structure is where lasting change begins.</p>

<div class="cta-box">
  <h3>From institutional frameworks to participation</h3>
  <p>Systemic change does not begin with declarations alone — it begins where everyday economic choices are made. The Citizen's Dividend for Nature is one practical expression of this direction, exploring how ecological value can become visible within ordinary transactions. Participation, refinement, and shared learning are part of shaping value signals that reflect the realities upon which all prosperity depends.</p>
</div>

<div class="framing-box">
  <h3>Key concepts</h3>
  <p><strong>Natural capital</strong><br>
  The ecological systems — climate stability, biodiversity, soil, oceans, water cycles — that underpin all economic activity.</p>

  <p><strong>Value signals</strong><br>
  The cues that shape everyday decisions — prices, incentives, returns, rules — indicating what the system rewards and what it treats as secondary.</p>

  <p><strong>Economic legibility</strong><br>
  The extent to which a contribution is visible and recognised within the economic system — not only morally, but in the way benefits and costs are accounted for.</p>

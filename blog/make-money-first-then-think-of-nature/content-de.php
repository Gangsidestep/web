  <h3>Vom Rahmen zur Teilhabe</h3>
  <p>Systemischer Wandel beginnt nicht mit Erklärungen — er beginnt dort, wo alltägliche wirtschaftliche Entscheidungen getroffen werden. Die Bürgerdividende für die Natur ist ein praktischer Ausdruck dieser Richtung: Sie untersucht, wie ökologischer Wert in gewöhnlichen Transaktionen sichtbar werden kann. Teilhabe, Weiterentwicklung und gemeinsames Lernen gehören dazu, Wertsignale zu gestalten, die die Realitäten widerspiegeln, von denen jeder Wohlstand abhängt.</p>

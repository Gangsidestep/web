  <p><strong>Naturkapital</strong><br>
  Die ökologischen Systeme — Klimastabilität, Biodiversität, Böden, Ozeane, Wasserkreisläufe — die jede wirtschaftliche Tätigkeit tragen.</p>

  <p><strong>Wertsignale</strong><br>
  Die Hinweise, die alltägliche Entscheidungen prägen — Preise, Anreize, Renditen, Regeln — und zeigen, was das System belohnt und was es nachordnet.</p>

  <p><strong>Wirtschaftliche Sichtbarkeit</strong><br>
  Das Ausmaß, in dem ein Beitrag im Wirtschaftssystem sichtbar und anerkannt wird — nicht nur moralisch, sondern auch in der Art und Weise, wie Nutzen und Kosten erfasst werden.</p>
